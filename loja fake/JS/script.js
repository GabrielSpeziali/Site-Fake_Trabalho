function scrollToLeft(id) {
    const el = document.getElementById('productScroll');
    el.scrollBy({ left: -300, behavior: 'smooth' });
  }
  
  function scrollToRight(id) {
    const el = document.getElementById('productScroll');
    el.scrollBy({ left: 300, behavior: 'smooth' });
  }
  
  
  
  
